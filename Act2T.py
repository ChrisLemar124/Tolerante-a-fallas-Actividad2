def multiplicar_matrices(matriz_A, matriz_B):
    filas_A = len(matriz_A)
    columnas_A = len(matriz_A[0])
    filas_B = len(matriz_B)
    columnas_B = len(matriz_B[0])

    for i in range(filas_A):
        if len(matriz_A[i]) != columnas_A:
            raise ValueError("La matriz A no es rectangular")

    for i in range(filas_B):
        if len(matriz_B[i]) != columnas_B:
            raise ValueError("La matriz B no es rectangular")

    if columnas_A != filas_B:
        raise ValueError("No concuerdan las dimesiones de las matrices no se puede multiplicar")
        

    a = np.array(matriz_A)
    b = np.array(matriz_B)

    resultado = np.dot(a,b)
    return resultado

def guardar_matriz_resultasdo(matriz, nombre_archivo):
    with open(nombre_archivo, "wb") as f:
        pickle.dump(matriz, f)

